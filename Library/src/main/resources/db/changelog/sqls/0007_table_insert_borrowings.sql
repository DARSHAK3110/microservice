CREATE TABLE BORROWINGS(
	BORROWING_ID BIGINT PRIMARY KEY AUTO_INCREMENT,
	BOOK_ID BIGINT,
	MEMBER_ID BIGINT,
	BORROWING_DATE DATE,
	DUE_DATE DATE,
	RETURN_DATE DATE,
	FOREIGN KEY (BOOK_ID) REFERENCES BOOKS(BOOK_ID),
	FOREIGN KEY (MEMBER_ID) REFERENCES MEMBERS(MEMBER_ID)
);