CREATE TABLE BOOKS(
	ISBN BIGINT PRIMARY KEY,
	TITLE VARCHAR(16),
	AUTHOR_ID BIGINT,
	PUBLICATION_DATE DATE,
	TOTAL_COPIES BIGINT,
	AVAILABLE_COPIES BIGINT,
	UPLOAD_ID BIGINT,
	FOREIGN KEY(UPLOAD_ID) REFERENCES UPLOADS(UPLOAD_ID),
	FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHORS(AUTHOR_ID)
);
